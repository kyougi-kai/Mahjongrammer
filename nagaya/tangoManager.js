function addMeansToTango(csvLines) {
    for (const line of csvLines) {
        // 1行をカンマで分割
        const [word, meaning] = line.split(',');

        if (word && meaning && tango[word]) {
            tango[word].hinsi = ['名詞'];
            tango[word].means = { 名詞: meaning.trim() };
        }
    }
}

